    DefaultTableModel tblModelSach;
    public void TaoTable (DSSach ds) {
        tblModelSach = new DefaultTableModel();
        String tieuDe[] = {"Mã sách", "Tên sách", "Tác giả", "NXB", "Giá"};
        tblModelSach.setColumnIdentifiers(tieuDe);
        // Đổ dliệu vào bảng
        tblModelSach.setRowCount(0);
        
        for  (int i = 0; i < ds.SLSach(); i ++) {
            Sach s = ds.LayThongTin(i);
            String [] rows = {String.valueOf(s.getMaSach()), s.getTenSach(), s.getTacGia(), s.getNXB(), String.valueOf(s.getGia())};
            tblModelSach.addRow(rows);
        }
        tblThongTin.setModel(tblModelSach);
        setVisible(true);
    }

    public static boolean isNumberic(String str) {
        try {
            Integer.parseInt(str);
                return true;
        } catch(NumberFormatException e) {
            return false;
        }
    }
