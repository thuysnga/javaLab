    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if ((txtMaSach.getText().equals("")) || (txtTenSach.getText().equals("")) || (txtTacGia.getText().equals("")) || (txtGia.getText().equals("")) || (txtNXB.getText().equals("")))
                JOptionPane.showConfirmDialog(null, "Vui lòng nhập đầy đủ thông tin","OK", JOptionPane.CLOSED_OPTION);
            else {
                if ((isNumberic(txtGia.getText())) == false || (isNumberic(txtMaSach.getText())) == false)
                    JOptionPane.showConfirmDialog(null,"Vui lòng nhập mã sách, giá sách là số nguyên","OK", JOptionPane.CLOSED_OPTION);
                else {
                    Sach s = new Sach();
                    s.setMaSach(Integer.parseInt(txtMaSach.getText()));
                    s.setTenSach(txtTenSach.getText());
                    s.setTacGia(txtTacGia.getText());
                    s.setNXB(txtNXB.getText());
                    s.setGia(Integer.parseInt(txtGia.getText()));
                    ds.ThemSach(s);
                    String [] rows = {String.valueOf(s.getMaSach()), s.getTenSach(), s.getTacGia(), s.getNXB(), String.valueOf(s.getGia())};
                    tblModelSach.addRow(rows);
                }
            }
    }//GEN-LAST:event_btnThemActionPerformed

